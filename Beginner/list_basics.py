my_list = ['abcd', '786', 'efg', '1234']
tinylist = ['john', 123]

print(my_list[0])        # first element
print(my_list[1:3])      # slicing from index 1 to 2
print(my_list[2:])       # from index 2 to end

print(tinylist*2)
print(my_list [0:4])

print(tinylist * 2)      # repeat list twice
print(my_list + tinylist)  # combine lists